                            <p>
                            AMD has steadily been gaining market share ever since the release of their Athlon last August, their Thunderbird last June and their Duron shortly thereafter. The company has come from a microprocessor manufacturer that focused on supplying processors for the value market segment to a manufacturer that is helping to drive the desktop market in both performance and value segments.
                            </p>
                            <p>
                            Intel gave us a very pleasant surprise at the Spring 2000 Intel Developer Forum (IDF) in February: for the first time the Willamette's architecture was discussed in a decent amount of detail. However, Intel has remained fairly quiet about the Willamette until now.
                            </p>
                            <p>
                            The P6 architecture, first introduced with the Pentium Pro in 1995 has been the basis for Intel's processors ever since then. The Pentium II, the Celeron, the Pentium III and even the newest Celerons all have their foundations in an architecture that has hit the 5-year old mark. As we described back in February, the Pentium 4 will be the first IA-32 (32-bit Intel Architecture) processor from Intel to use a non-P6 architecture in recent times.
                            </p>
                            <p>
                            By the time the Pentium 4 hits the streets, the fastest Pentium III will most likely still be the 1.13GHz part we reviewed not too long ago, and with the P4 debuting with at least two speed grades (1.4GHz and above is Intel's official statement, but also remember that we saw a 1.5GHz Pentium 4 in February) there should be a performance delta between the two upon its launch.
                            </p>
